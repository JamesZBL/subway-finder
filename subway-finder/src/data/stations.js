export const getStationsForDirection = (lineId, directionId) => {
  if (!stationData[lineId]) return []
  
  const directions = stationData[lineId].directions
  if (!directions) return []
  
  const direction = directions.find(dir => dir.id === directionId)
  return direction ? direction.stations : []
}

